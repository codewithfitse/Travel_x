import mongoose, { Types } from "mongoose";

const UserSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  isAdmin: {
    type: Boolean,
    default: false,
  },
  lastLogin: {
      type: Date,
      default: null,
    },
},{ timestamps: true });

const UserLogin = mongoose.model("Logins", UserSchema);
export default UserLogin;
